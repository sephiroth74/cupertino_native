/// Visual styles for [CNButton] and related controls.
enum CNButtonStyle {
  /// Platform default button style.
  automatic,

  /// Minimal, text-only style.
  plain,

  /// Borderless button style.
  borderless,

  /// Link-style button.
  link,

  /// Bordered button style.
  bordered,

  /// Prominent bordered (accent-colored) style.
  borderedProminent,

  /// Glass effect for modern Apple UI styles.
  glass,

  /// More prominent glass effect.
  prominentGlass,

  /// Accessory bar button style, typically used in toolbars or accessory views.
  accessoryBar,

  /// Accessory bar button style with an action emphasis, often used for primary actions in accessory bars.
  accessoryBarAction,
}
